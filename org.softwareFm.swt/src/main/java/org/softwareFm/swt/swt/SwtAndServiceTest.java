package org.softwareFm.swt.swt;

import java.util.concurrent.TimeUnit;

import org.softwareFm.common.services.IServiceExecutor;

abstract public class SwtAndServiceTest extends SwtTest {

	protected IServiceExecutor service;

	@Override
	protected void setUp() throws Exception {
		super.setUp();
		service = IServiceExecutor.Utils.defaultExecutor();
	}

	@Override
	protected void tearDown() throws Exception {
		service.shutdownAndAwaitTermination(2, TimeUnit.SECONDS);
		super.tearDown();
	}

}